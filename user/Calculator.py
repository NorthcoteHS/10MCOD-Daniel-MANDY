aa = int(input('enter a number: '))
bb = int(input('enter another number: '))
cc = str(input('enter an operator: '))
if cc == '+':
    print(aa+bb)
if cc == '-':
    print(aa-bb)
if cc == '/':
    print(aa/bb)
if cc == '*':
    print(aa*bb)
if cc == '**':
    print(aa**bb)
















































